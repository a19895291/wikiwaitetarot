// 牌陣定義。座標約定：
//   x = 欄（0 起算，向右遞增）
//   y = 列（0 起算，向下遞增）
//   cols / rows = 該牌陣的格線尺寸（渲染時用來計算每格寬高並置中）
//   rotated = true 時該張牌旋轉 90°（凱爾特十字的「阻礙」牌）
//   hint = 牌位意義（解讀面板顯示用）
//
// 渲染建議：cellW = boardWidth / cols；cellH = cellW * 1.5（牌 2:3）；
//   第 i 張牌左上座標 = (x * cellW, y * cellH)，整體再置中。

